a = 10
b = 5
c = 2

# AND operator (True if both conditions true)
print("AND operator:", a > b and b > c)   # True

# OR operator (True if any one condition true)
print("OR operator:", a < b or b > c)     # True

# NOT operator (Negates the result)
print("NOT operator:", not(a > b))        # False
